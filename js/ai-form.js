// js/ai-form.js (DEBUG-VERSION mit ausf√ºhrlichen Logs)

import { initBookingModal, showStep } from './booking.js';

export const initAiForm = () => {
    console.log("üöÄ initAiForm gestartet");

    const aiForm = document.getElementById('ai-form');
    if (!aiForm) {
        console.warn("‚ö†Ô∏è initAiForm: #ai-form nicht gefunden!");
        return;
    }

    const aiQuestion = document.getElementById('ai-question');
    const aiStatus = document.getElementById('ai-status');
    const modalOverlay = document.getElementById('ai-response-modal');
    const responseArea = document.getElementById('ai-chat-history');
    const closeButtons = document.querySelectorAll('#close-ai-response-modal-top, #close-ai-response-modal-bottom');

    console.log("üîß Elements gefunden:", {
        aiForm: !!aiForm,
        aiQuestion: !!aiQuestion,
        modalOverlay: !!modalOverlay,
        responseArea: !!responseArea
    });

    // Modal-Steuerung
    const showModal = () => {
        if (!modalOverlay) return;
        
        console.log("üéØ Zeige AI-Modal");
        modalOverlay.classList.add('visible');
        modalOverlay.style.display = 'flex';
        modalOverlay.style.opacity = '1';
        modalOverlay.style.visibility = 'visible';
        modalOverlay.style.pointerEvents = 'auto';
        
        document.body.style.overflow = 'hidden';
        document.body.classList.add('no-scroll');
    };

    const hideModal = () => {
        if (!modalOverlay) return;
        
        console.log("‚ùé Verstecke AI-Modal");
        modalOverlay.classList.remove('visible');
        modalOverlay.style.display = 'none';
        modalOverlay.style.opacity = '0';
        modalOverlay.style.visibility = 'hidden';
        modalOverlay.style.pointerEvents = 'none';
        
        document.body.style.overflow = '';
        document.body.classList.remove('no-scroll');
    };

    // VERBESSERTE Booking-Modal-Funktion mit detailliertem Logging
    const launchBookingModal = async () => {
        console.log("üìÖ launchBookingModal gestartet");
        
        try {
            // Verstecke zun√§chst das AI-Modal
            console.log("üîí Verstecke AI-Modal");
            hideModal();
            
            // Pr√ºfe Modal-Container
            const modalContainer = document.getElementById('modal-container');
            console.log("üì¶ Modal-Container gefunden?", !!modalContainer);
            
            if (!modalContainer) {
                throw new Error('Modal-Container nicht gefunden - pr√ºfe ob modals.html geladen ist');
            }
            
            // Pr√ºfe, ob booking-modal.html bereits geladen ist
            let bookingModal = document.getElementById('booking-modal');
            console.log("üìã Booking-Modal bereits vorhanden?", !!bookingModal);
            
            if (!bookingModal) {
                console.log("üìÑ Lade booking-modal.html...");
                
                const response = await fetch('/booking-modal.html');
                console.log("üì° Fetch Response Status:", response.status);
                
                if (!response.ok) {
                    throw new Error(`HTTP ${response.status}: Booking-Modal konnte nicht geladen werden`);
                }
                
                const html = await response.text();
                console.log("üìÑ HTML geladen, L√§nge:", html.length);
                console.log("üìÑ HTML Anfang:", html.substring(0, 100));
                
                modalContainer.insertAdjacentHTML('beforeend', html);
                console.log("‚úÖ HTML eingef√ºgt");
                
                // Suche das Modal erneut
                bookingModal = document.getElementById('booking-modal');
                console.log("üìã Booking-Modal nach Laden gefunden?", !!bookingModal);
                
                if (!bookingModal) {
                    console.error("‚ùå Booking-Modal nach dem Laden nicht gefunden!");
                    console.log("üîç Alle IDs im Modal-Container:", Array.from(modalContainer.querySelectorAll('[id]')).map(el => el.id));
                    throw new Error('Booking-Modal nach dem Laden nicht gefunden');
                }
                
                console.log("‚úÖ Booking-Modal HTML geladen");
                
                // Initialisiere das Booking-System
                console.log("üîß Initialisiere Booking-System...");
                initBookingModal();
                console.log("‚úÖ Booking-System initialisiert");
            } else {
                console.log("‚ôªÔ∏è Booking-Modal bereits vorhanden, verwende existierendes");
            }
            
            // Zeige das Booking-Modal
            console.log("üëÄ Zeige Booking-Modal...");
            bookingModal.style.display = 'flex';
            bookingModal.style.opacity = '1';
            bookingModal.style.visibility = 'visible';
            bookingModal.style.pointerEvents = 'auto';
            console.log("‚úÖ Booking-Modal angezeigt");
            
            // Starte mit dem ersten Schritt
            console.log("üéØ Starte ersten Buchungsschritt...");
            showStep('step-day-selection');
            console.log("‚úÖ Erster Schritt aktiviert");
            
        } catch (error) {
            console.error("‚ùå Fehler beim Laden des Booking-Modals:", error);
            console.error("Stack:", error.stack);
            
            // Zeige Fehler im AI-Chat und bringe das AI-Modal zur√ºck
            addMessageToHistory(`Entschuldigung, beim √ñffnen des Buchungsfensters ist ein Fehler aufgetreten: ${error.message}`, 'ai');
            showModal();
        }
    };

    const addMessageToHistory = (message, sender) => {
        if (!responseArea) {
            console.warn("‚ö†Ô∏è responseArea nicht verf√ºgbar f√ºr Message:", message);
            return;
        }

        console.log(`üí¨ F√ºge ${sender}-Nachricht hinzu:`, message.substring(0, 50) + "...");

        const messageDiv = document.createElement('div');
        messageDiv.className = `chat-message ${sender}`;
        
        if (message.includes('<') && message.includes('>')) {
            messageDiv.innerHTML = message;
        } else {
            messageDiv.textContent = message;
        }
        
        responseArea.appendChild(messageDiv);
        responseArea.scrollTop = responseArea.scrollHeight;
        
        console.log("‚úÖ Nachricht hinzugef√ºgt, aktuelle Anzahl:", responseArea.children.length);
    };

    const initializeChat = (initialMessage) => {
        if (!responseArea) {
            console.warn("‚ö†Ô∏è responseArea nicht verf√ºgbar f√ºr initializeChat");
            return;
        }
        
        console.log("üÜï Initialisiere Chat mit:", initialMessage.substring(0, 50) + "...");
        responseArea.innerHTML = '';
        addMessageToHistory(initialMessage, 'ai');
    };

    // VERBESSERTE API-Kommunikation mit detailliertem Logging
    const handleFormSubmit = async (event) => {
        event.preventDefault();
        console.log("üì® handleFormSubmit ausgel√∂st");

        const question = aiQuestion.value.trim();
        if (!question) {
            console.warn("‚ö†Ô∏è Leere Eingabe");
            return;
        }

        console.log("‚ùì Frage:", question);

        aiStatus.textContent = 'Evita denkt nach...';
        aiStatus.style.display = 'block';
        aiQuestion.disabled = true;
        aiForm.querySelector('button').disabled = true;

        try {
            console.log("üåê Sende Anfrage an Evita:", question);
            
            const response = await fetch('/api/ask-gemini', {
                method: 'POST',
                headers: { 'Content-Type': 'application/json' },
                body: JSON.stringify({ prompt: question }),
            });

            console.log("üì° Response Status:", response.status);

            if (!response.ok) {
                throw new Error(`HTTP-Fehler: ${response.status}`);
            }

            const data = await response.json();
            console.log("üì® Evita Response:", data);

            // DETAILLIERTE Pr√ºfung auf Booking-Aktion
            console.log("üîç Pr√ºfe auf Booking-Aktion...");
            console.log("üîç data.action:", data.action);
            console.log("üîç data.action === 'start_booking':", data.action === 'start_booking');

            if (data.action === 'start_booking') {
                console.log("üéØ BOOKING-AKTION ERKANNT!");
                console.log("üìù Booking-Nachricht:", data.message);
                
                initializeChat(data.message);
                showModal();
                
                console.log("‚è∞ Starte Booking in 2 Sekunden...");
                setTimeout(() => {
                    console.log("üöÄ Timeout erreicht, starte Booking...");
                    launchBookingModal();
                }, 2000);
                return;
            } else {
                console.log("‚ÑπÔ∏è Keine Booking-Aktion erkannt, normale Antwort");
            }

            // Normale Antwort
            if (data.answer) {
                console.log("üí¨ Verarbeite normale Antwort");
                initializeChat(data.answer);
                showModal();
            } else {
                console.warn("‚ö†Ô∏è Keine Antwort in Response");
                initializeChat("Entschuldigung, ich konnte keine Antwort generieren.");
                showModal();
            }

        } catch (error) {
            console.error('‚ùå Fehler bei Evita-Anfrage:', error);
            aiStatus.textContent = 'Ein Fehler ist aufgetreten.';
            initializeChat("Entschuldigung, ich habe gerade technische Schwierigkeiten. Bitte versuche es sp√§ter noch einmal.");
            showModal();
        } 
        finally {
            aiQuestion.value = '';
            aiStatus.style.display = 'none';
            aiQuestion.disabled = false;
            aiForm.querySelector('button').disabled = false;
            console.log("üîÑ Formular zur√ºckgesetzt");
        }
    };

    // VERBESSERTE Chat-Funktion mit detailliertem Logging
    const handleChatSubmit = async (event) => {
        event.preventDefault();
        console.log("üí¨ handleChatSubmit ausgel√∂st");

        const chatInput = document.getElementById('ai-chat-input');
        if (!chatInput) {
            console.warn("‚ö†Ô∏è Chat-Input nicht gefunden");
            return;
        }

        const userInput = chatInput.value.trim();
        if (!userInput) {
            console.warn("‚ö†Ô∏è Leere Chat-Eingabe");
            return;
        }

        console.log("üí¨ Chat-Eingabe:", userInput);

        addMessageToHistory(userInput, 'user');
        chatInput.value = '';

        try {
            console.log("üåê Sende Chat-Anfrage an Evita");
            const response = await fetch('/api/ask-gemini', {
                method: 'POST',
                headers: { 'Content-Type': 'application/json' },
                body: JSON.stringify({ prompt: userInput })
            });

            const data = await response.json();
            console.log("üì® Chat Response:", data);
            
            // DETAILLIERTE Pr√ºfung auf Booking-Aktion im Chat
            console.log("üîç Chat: Pr√ºfe auf Booking-Aktion...");
            console.log("üîç Chat: data.action:", data.action);
            
            if (data.action === 'start_booking') {
                console.log("üéØ CHAT: BOOKING-AKTION ERKANNT!");
                addMessageToHistory(data.message, 'ai');
                
                console.log("‚è∞ Chat: Starte Booking in 2 Sekunden...");
                setTimeout(() => {
                    console.log("üöÄ Chat: Timeout erreicht, starte Booking...");
                    launchBookingModal();
                }, 2000);
                return;
            }

            // Normale Chat-Antwort
            if (data.answer) {
                console.log("üí¨ Chat: Normale Antwort");
                addMessageToHistory(data.answer, 'ai');
            } else if (data.message) {
                console.log("üí¨ Chat: Message-Feld");
                addMessageToHistory(data.message, 'ai');
            } else {
                console.warn("‚ö†Ô∏è Chat: Keine Antwort");
                addMessageToHistory('Entschuldigung, ich konnte keine Antwort generieren.', 'ai');
            }

        } catch (error) {
            console.error('‚ùå Fehler bei Chat-Anfrage:', error);
            addMessageToHistory('Entschuldigung, da ist ein technischer Fehler aufgetreten.', 'ai');
        }
    };

    // Event Listener registrieren
    aiForm.addEventListener('submit', handleFormSubmit);
    console.log("‚úÖ AI-Form Submit-Listener registriert");

    // Chat-Form Event Listener
    document.addEventListener('submit', (e) => {
        if (e.target.id === 'ai-chat-form') {
            console.log("üìù Chat-Form Submit erkannt");
            handleChatSubmit(e);
        }
    });
    console.log("‚úÖ Chat-Submit-Listener registriert");

    // Close-Button Event Listeners
    closeButtons.forEach(button => {
        button.addEventListener('click', () => {
            console.log("‚ùé AI-Modal Close-Button geklickt");
            hideModal();
        });
    });
    console.log("‚úÖ Close-Button-Listener registriert");

    console.log("‚úÖ Evita AI-Form vollst√§ndig initialisiert");
};
